/**
 * Добавляет стандартный шрифт helvetica для кириллицы в PDF
 * @param {jsPDF} doc - Экземпляр jsPDF документа
 * @returns {jsPDF} - Настроенный документ с поддержкой кириллицы
 */
export const addDejaVuSansFont = (doc) => {
  try {
    // Вместо попытки добавить неполный шрифт, просто устанавливаем стандартный
    doc.setLanguage('ru');
    doc.setFont('helvetica', 'normal');
    
    return doc;
  } catch (error) {
    console.error("Ошибка при настройке шрифта для кириллицы:", error);
    
    // В случае ошибки возвращаем документ с стандартным шрифтом
    doc.setFont("helvetica", "normal");
    return doc;
  }
}; 